# ===== 2. 計算邏輯 =====
def time_to_min(t):
    try:
        h, m = map(int, t.split(":"))
        return h * 60 + m
    except: return 9999

def get_direction(stations, start, end):
    return "go" if stations.index(start) < stations.index(end) else "back"

# ===== 3. 網頁介面 =====
st.set_page_config(page_title="大台南公車查詢系統", layout="wide")
st.title("🚌 大台南公車即時時刻表")

# 側邊欄或上方選擇
route_name = st.selectbox("請選擇公車路線", list(routes.keys()))
data = routes[route_name]

col1, col2 = st.columns(2)
with col1:
    start_station = st.selectbox("起點站", data["stations"])
with col2:
    end_station = st.selectbox("終點站", data["stations"])

if st.button("🔍 開始查詢"):
    if start_station == end_station:
        st.error("❌ 起點跟終點不能一樣喔！")
    else:
        direction = get_direction(data["stations"], start_station, end_station)
        now_min = time_to_min(datetime.now().strftime("%H:%M"))
        
        # 篩選班次
        valid_buses = []
        for b in data["buses"]:
            if b["方向"] == direction and start_station in b and end_station in b:
                valid_buses.append(b)
        
        if not valid_buses:
            st.warning("⚠️ 抱歉，這兩個站點之間目前沒有建立詳細時刻表。")
        else:
            # 找到下一班
            valid_buses.sort(key=lambda x: time_to_min(x[start_station]))
            next_bus = next((b for b in valid_buses if time_to_min(b[start_station]) > now_min), None)
            
            if next_bus:
                wait_time = time_to_min(next_bus[start_station]) - now_min
                st.success(f"🔔 下一班 **{next_bus['班次']}** 號車，預計 **{wait_time}** 分鐘後抵達 **{start_station}**")
                st.write(f"📅 預計發車時間：{next_bus[start_station]}")
            else:
                st.info("🌙 今日班次已收班。")
            
            # 顯示今日時刻表
            with st.expander("📅 查看今日完整時刻表"):
                for b in valid_buses:
                    st.write(f"班次 {b['班次']}: {b[start_station]} ➜ {b[end_station]}")
